function OnLoadBefore(dataset)
{
	if(dataset == dsMAIN)
	{
		dataset.DataID = sSelectPageName + D_P1("ACT","MAIN");
	}
	else if(dataset == dsBANK_CLS)
	{
		dataset.DataID = sSelectPageName + D_P1("ACT","BANK_CLS");
	}
}

